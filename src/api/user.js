import axios from '@/libs/api.request'
import md5 from 'js-md5'

export const login = ({ username, pswd, rememberMe }) => {
  const data = {
    username,
    pswd: md5(pswd),
    rememberMe
  }
  return axios.request({
    url: '/u/submitLogin.do',
    data,
    method: 'post'
  })
}

export const getUserInfo = (token) => {
  return axios.request({
    url: 'get_info',
    params: {
      token
    },
    method: 'get'
  })
}

// 退出
export const logout = (params) => {
  return axios.request({
    url: '/u/logout.do',
    method: 'post',
    data: { ...params }
  })
}

